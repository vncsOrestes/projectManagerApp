    private void jTable1TableTasksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1TableTasksMouseClicked
        // TODO add your handling code here:
        
        int rowIndex = jTable1TableTasks.rowAtPoint(evt.getPoint());
        int columnIndex = jTable1TableTasks.columnAtPoint(evt.getPoint());
        Task task = taskModel.getTasks().get(rowIndex);
        
        switch (columnIndex) {
            case 3:
                taskController.update(task);
                break;
            case 4:
                break;
            case 5:
                taskController.removeById(task.getId());
                taskModel.getTasks().remove(task);
                
                int projectIndex = jList1Projects.getSelectedIndex();
                Project project = (Project) projecstModel.get(projectIndex);
                loadTasks(project.getId());
                
                break;
        }
    }//GEN-LAST:event_jTable1TableTasksMouseClicked

