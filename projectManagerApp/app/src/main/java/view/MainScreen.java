    private void showJTableTasks (boolean hasTasks) {
        if(hasTasks) {
            if(jPanel6EmptyList.isVisible()) {
                jPanel6EmptyList.setVisible(false);
                jPanel5.remove(jPanel6EmptyList);
            }
            
            jPanel5.add(jScrollPane1Tasks);
            jScrollPane1Tasks.setVisible(true);
            jScrollPane1Tasks.setSize(jPanel5.getWidth(), jPanel5.getHeight());
        } else {
            if(jScrollPane1Tasks.isVisible()) {
                jScrollPane1Tasks.setVisible(false);
                jPanel5.remove(jScrollPane1Tasks);
            }
            
            jPanel5.add(jPanel6EmptyList);
            jPanel6EmptyList.setVisible(true);
            jPanel6EmptyList.setSize(jPanel5.getWidth(), jPanel5.getHeight());
        }
        
    }
